USE productCompany;

DROP TABLE IF EXISTS `PRODUCT_INFO`;
DROP TABLE IF EXISTS `PRODUCT_CATEGORY`;

CREATE TABLE `PRODUCT_CATEGORY`
(
    `CATEGORY_CODE`	MEDIUMINT NOT NULL AUTO_INCREMENT COMMENT '제품분류코드',
    `CATEGORY_NAME`	VARCHAR(100) NOT NULL COMMENT '제품분류명',
 PRIMARY KEY ( `CATEGORY_CODE`)
)
 COMMENT = '제품분류';
 
 CREATE TABLE `PRODUCT_INFO`
(
    `PRODUCT_CODE`		MEDIUMINT NOT NULL AUTO_INCREMENT COMMENT '제품코드',
    `PRODUCT_NAME`		VARCHAR(50) NOT NULL COMMENT '제품명',
    `ORIGIN_COST`		VARCHAR(10) NOT NULL COMMENT '원가',
    `RELEASE_DATE`		VARCHAR(8) NOT NULL COMMENT '출시일',
    `DISCOUNT_RATE`		VARCHAR(3) COMMENT '할인율',
    `SALES_QUANTITY`	VARCHAR(15) NOT NULL COMMENT '판매량',
    `STOCK_QUANTITY`    VARCHAR(15) NOT NULL COMMENT '재고량',
    `CATEGORY_CODE`		VARCHAR(5) NOT NULL COMMENT '제품분류코드',
    `PRODUCTION_STATUS`	VARCHAR(1) NOT NULL COMMENT '생산상태',
 PRIMARY KEY ( `PRODUCT_CODE` )
)
 COMMENT = '제품정보';

 
